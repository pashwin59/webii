// Create an object
let person = {
    name: "Ashwin",
    age: 22,
    isStudent: true
};

// Access object properties
console.log(person.name);      // Ashwin
console.log(person.age);       // 22
console.log(person.isStudent); // true

// Add a new property
person.city = "Pokhara";
console.log(person.city);      // Pokhara

// Change a property
person.age = 23;
console.log(person.age);       // 23
